#!/bin/bash
# Скрипт для поиска файлов с заданным расширением в заданном каталоге с записью в заданный файл
# Проверяем наличие всех трёх аргументов
if [ $# -ne 3 ]; then
  echo "Использование: $0 <имя_файла_вывода> <каталог> <расширение>"
  exit 1
fi

output_file="$1"
directory="$2"
extension="$3"

# Проверяем существование каталога
if [ ! -d "$directory" ]; then
  echo "Ошибка: каталог '$directory' не существует."
  exit 1
fi

# Ищем файлы с заданным расширением и записываем в файл
find "$directory" -type f -name "*.${extension}" > "$output_file"

echo "Список файлов с расширением ${extension} из каталога ${directory} записан в ${output_file}."