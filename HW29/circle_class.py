# Работаем с кругами и их классами

# Импортируем модуль для работы с числом пи
import math

# Создаём класс Круг и методы вычисления площади и длины окружности
class Круг:
    def __init__(self, радиус, цвет):
        self.радиус = радиус
        self.цвет = цвет

    def вычислить_площадь(self):
        return math.pi * (self.радиус ** 2)

    def вычислить_длину_окружности(self):
        return 2 * math.pi * self.радиус

# Создаём несколько объектов класса Круг
круг1 = Круг(5, "красный")
круг2 = Круг(3, "желтый")
круг3 = Круг(7, "зелёный")

# Вызываем методы для каждого объекта и выводим результаты
print(f"Круг 1 (радиус = {круг1.радиус}, цвет = {круг1.цвет}):")
print(f"  Площадь: {круг1.вычислить_площадь():.2f}")
print(f"  Длина окружности: {круг1.вычислить_длину_окружности():.2f}\n")

print(f"Круг 2 (радиус = {круг2.радиус}, цвет = {круг2.цвет}):")
print(f"  Площадь: {круг2.вычислить_площадь():.2f}")
print(f"  Длина окружности: {круг2.вычислить_длину_окружности():.2f}\n")

print(f"Круг 3 (радиус = {круг3.радиус}, цвет = {круг3.цвет}):")
print(f"  Площадь: {круг3.вычислить_площадь():.2f}")
print(f"  Длина окружности: {круг3.вычислить_длину_окружности():.2f}")
