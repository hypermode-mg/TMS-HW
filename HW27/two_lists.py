# общие элементы, а затем создаёт новый список, содержащий только уникальные
# элементы

# Получаем первый список от пользователя
input_list1 = input("Введите элементы первого списка через пробел: ")
# Разделяем введённую строку на элементы и преобразуем в список
list1 = input_list1.split()

# Получаем второй список от пользователя
input_list2 = input("Введите элементы второго списка через пробел: ")
# Разделяем введённую строку на элементы и преобразуем в список
list2 = input_list2.split()

# Находим общие элементы с помощью пересечения множеств
common_items = set(list1) & set(list2)

# Преобразуем результат обратно в список
common_list = list(common_items)

# Создаём новый список, содержащий только уникальные элементы из обоих списков
# Объединяем оба списка, преобразуем в множество и обратно в список
uniq_items = list(set(list1 + list2))

# Выводим результаты
print("Общие элементы:", common_list)
